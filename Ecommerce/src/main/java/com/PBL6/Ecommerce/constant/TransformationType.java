package com.PBL6.Ecommerce.constant;

/**
 * Transformation Type Enum
 * Defines predefined image transformation sizes
 */
public enum TransformationType {
    /**
     * Thumbnail: 200x200 (fill)
     */
    THUMBNAIL,

    /**
     * Medium: 600x600 (fit)
     */
    MEDIUM,

    /**
     * Large: 1200x1200 (fit)
     */
    LARGE,

    /**
     * Original image without transformation
     */
    ORIGINAL
}
